  final String name;
  final String creator;
  final String image;
  final String desc;
  final Space space;

  const Boat(this.name, this.creator, this.image, this.desc, this.space);
}

class Space {
  final String length;
  final String beam;
  final String weight;
  final String fuel;

  const Space(this.length, this.beam, this.weight, this.fuel);
}

